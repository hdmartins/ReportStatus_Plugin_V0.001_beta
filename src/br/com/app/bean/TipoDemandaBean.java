package br.com.app.bean;

public class TipoDemandaBean 
{
	private int 	idTipoDemanda   ;
	private String 	nomeTipoDemanda ;
	private String	descTipoDemanda ;
	
	public int getIdTipoDemanda() {
		return idTipoDemanda;
	}
	public void setIdTipoDemanda(int idTipoDemanda) {
		this.idTipoDemanda = idTipoDemanda;
	}
	public String getNomeTipoDemanda() {
		return nomeTipoDemanda;
	}
	public void setNomeTipoDemanda(String nomeTipoDemanda) {
		this.nomeTipoDemanda = nomeTipoDemanda;
	}
	public String getDescTipoDemanda() {
		return descTipoDemanda;
	}
	public void setDescTipoDemanda(String descTipoDemanda) {
		this.descTipoDemanda = descTipoDemanda;
	}
	
	
}
